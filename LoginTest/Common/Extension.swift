import Foundation
import UIKit


extension UIColor {
    
    static var inactiveButtonColor: UIColor {
        get {
            return #colorLiteral(red: 1, green: 0.8331254125, blue: 0.4734505415, alpha: 1)
        }
    }
    
    static var activeButtonColor: UIColor {
        get {
            return #colorLiteral(red: 1, green: 0.5763723254, blue: 0, alpha: 1)
        }
    }
}
